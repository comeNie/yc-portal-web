    /**
     * 发送 POST 请求（HTTP），K-V形式
     * @param apiUrl API接口URL
     * @param params 参数map
     * @return
     */
    public static String doPost(CloseableHttpClient httpClient,String apiUrl, Map<String, Object> params)
            throws IOException, HttpStatusException {
        long startTime = System.currentTimeMillis();
        LOGGER.info("开始HttpUtil.post ,当前时间戳:{}",startTime);
        String httpStr = null;
        HttpPost httpPost = new HttpPost(apiUrl);
        CloseableHttpResponse response = null;

        try {
//            httpPost.setConfig(requestConfig);
            List<NameValuePair> pairList = new ArrayList<>(params.size());
            for (Map.Entry<String, Object> entry : params.entrySet()) {
                NameValuePair pair = new BasicNameValuePair(entry.getKey(), entry
                        .getValue().toString());
                pairList.add(pair);
            }
            httpPost.setEntity(new UrlEncodedFormEntity(pairList, Charset.forName("UTF-8")));
            long httpStart = System.currentTimeMillis();
            LOGGER.info("开始httpClient,当前时间戳:{}",httpStart);
            response = httpClient.execute(httpPost);
            long httpEnd = System.currentTimeMillis();
            LOGGER.info("结束httpClient,当前时间戳:{},用时:{}",httpEnd,(httpEnd-httpStart));
            int statusCode = response.getStatusLine().getStatusCode();
            LOGGER.info("http status:{},\r\nHttp responst:{}",statusCode,response.toString());
            if (statusCode!=200)
                throw new HttpStatusException("the http status code:"+statusCode);
            HttpEntity entity = response.getEntity();
            httpStr = EntityUtils.toString(entity, "UTF-8");
            LOGGER.debug("Http responst entiey:{}",httpStr);
        } catch (IOException e) {
            LOGGER.error("Http post io exception",e);
            throw e;
        } finally {
            if (response != null) {
                try {
                    EntityUtils.consume(response.getEntity());
                } catch (IOException e) {
                    LOGGER.error("Http post io exception",e);
                    throw e;
                }
            }
        }
        long endTime = System.currentTimeMillis();
        LOGGER.info("结束HttpUtil.post ,当前时间戳:{},用时:{}",endTime,(endTime-startTime));
        return httpStr;
    }
    
