package com.ai.yc.protal.web.model;

public class StepInfo {
	/**
	 * 操作类型
	 */
	private String type;
	/**
	 * 步骤编号
	 */
	private String no;
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	

}
